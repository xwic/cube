package de.xwic.cube.webui.controls.filter;

/**
 * @author bogdan
 *
 */
public class FilterGroupException extends Exception {

	public FilterGroupException() {
	}

	public FilterGroupException(String arg0) {
		super(arg0);
	}

	public FilterGroupException(Throwable arg0) {
		super(arg0);
	}

	public FilterGroupException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
